    private void jButton_Edit_ClientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Edit_ClientActionPerformed
        //edit the Client data

        // get the Client data

        String fname = jTextField_FName.getText();
        String lname = jTextField_LName.getText();
        String phone = jTextField_Phone.getText();
        String email = jTextField_Email.getText();
        String adresse = jTextArea_Adresse.getText();

        P_CLIENT client =new P_CLIENT();

        try{
            //before editing the Client data we need to check if the required data are empty
            // required data -> first name,last name,phone ,and adresse
            int clientId = Integer.valueOf(jTextField_Id.getText());
            if(fname.trim().equals("")||lname.trim().equals("")||phone.trim().equals("")||adresse.trim().equals(""))
            {
               JOptionPane.showMessageDialog(null, "Enter the Required client Information [ firstname,Lastname,adresse,phone,email]","Edit  client",1);

            }
            else {

                if(  client.editClientData(new P_CLIENT (clientId,fname,lname,phone,email,adresse))){
                    
                    JOptionPane.showMessageDialog(null, " client data  Edited To The System","Edit client",1);

                }
                else
                {

                    JOptionPane.showMessageDialog(null, "client data  not Edited To The System","Edit client",2);
                }
            }
        }catch (Exception ex)
        {
            JOptionPane.showMessageDialog(null,ex.getMessage()+"Enter a Valid client ID","Invalid Id" ,0);
        }

    }//GEN-LAST:event_jButton_Edit_ClientActionPerformed

