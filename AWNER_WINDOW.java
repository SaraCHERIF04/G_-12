   // we will create function to populate the jtable with all the owners data using the arraylist 
    //after that we will make the user select an owner from the jtable all information will be displayed in jtextfields
    public void fillJtablewithOwnersdata()
    {
        p_Owner owner =new p_Owner();
        ArrayList<p_Owner> ownerList = owner.ownerList();
        //the jtable columns
        String[] colNames = {"ID","First Name","Last Name","Phone","Email","Adresse"};
        //the jtable row 
        Object [][] rows = new Object [ownerList.size()][6];
        
        //add data  form the list to the rows 
        for(int i =0;i <ownerList.size();i++ )
        {
            rows[i][0]=ownerList.get(i).getId();
            rows[i][1]=ownerList.get(i).getFName();
            rows[i][2]=ownerList.get(i).getLName();
            rows[i][3]=ownerList.get(i).getPhone();
            rows[i][4]=ownerList.get(i).getEmail();
            rows[i][5]=ownerList.get(i).getAdresse();
               
        }
        
        DefaultTableModel model = new DefaultTableModel(rows,colNames);
        jTable1.setModel(model);
        // changÃ© the jtable row height 
        jTable1.setRowHeight(40);
        // change the jtable selection background 
        jTable1.setSelectionBackground( Color.PINK);
        //change the id column width 
        jTable1.getColumnModel().getColumn(0).setPreferredWidth(25);
         //change the address column width 
        jTable1.getColumnModel().getColumn(5).setPreferredWidth(150);        
        }

