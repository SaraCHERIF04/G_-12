    private void jButton_Edit_OwnerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Edit_OwnerActionPerformed
        //edit the owner data
        
        // get the owner data
    
   String fname = jTextField_FName.getText();
   String lname = jTextField_LName.getText();
   String phone = jTextField_Phone.getText();
   String email = jTextField_Email.getText();
   String adresse = jTextArea_Adresse.getText();
   
   p_Owner owner =new p_Owner ();
   
   try{
      
        int ownerId = Integer.valueOf(jTextField_Id.getText());
        //before editing the owner data we need to check if the required data are empty 
       // required data -> first name,last name,phone ,and adresse
         if(fname.trim().equals("")||lname.trim().equals("")||phone.trim().equals("")||adresse.trim().equals(""))
         {
         JOptionPane.showMessageDialog(null, "Enter the Required Owner Information [ firstname,Lastname,adresse,phone,email]","Edit  Owner",1);
       
         }
         else {
       
         if(  owner.editOwnerData(new p_Owner (ownerId,fname,lname,phone,email,adresse)))
         {
          JOptionPane.showMessageDialog(null, "owner edited To The System","edit Owner",1);
       
          }
         else 
         {

          JOptionPane.showMessageDialog(null, "Owner data  not Edited To The System","Edit Owner",2);
          }
         }
     }catch (Exception ex) 
     {
         JOptionPane.showMessageDialog(null,ex.getMessage()+"Enter a Valid owner ID","Invalid Id" ,0);     
     }

   

        
    }//GEN-LAST:event_jButton_Edit_OwnerActionPerformed

